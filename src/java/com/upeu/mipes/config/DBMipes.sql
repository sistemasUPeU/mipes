        IF IDA > 0 THEN
            IF(SELECT COUNT(*) FROM DETDISCIPULADOR WHERE idDISCIPULADOR=IDA AND idDISCIPULADO=IDD) =0 THEN
            START TRANSACTION;
                INSERT INTO DETDISCIPULADOR (idDISCIPULADOR, idDISCIPULADO, ESTADO) VALUES(IDA,IDD,'1');
            COMMIT;
            END IF;
        END IF;
    END IF;
