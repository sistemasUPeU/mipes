--GET_INFO_GP
CREATE PROCEDURE GET_INFO_GP(IDPERSONAi INTEGER)
BEGIN
    SELECT M.*, G.NOMBRE AS GRUPO,G.IDLIDER AS CARGO, E.NOMBRE AS ESCUELA,
    I.NOMBRE AS IGLESIA, GET_PORCENT_ASIST(M.IDMIEMBROGP) AS PORCENTAJE
